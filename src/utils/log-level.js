class LogLevel {
    static get EMERGENCY() {
        return 0x00;
    }
    static get ALERT() {
        return 0x01;
    }
    static get CRITICAL() {
        return 0x02;
    }
    static get ERROR() {
        return 0x03;
    }
    static get WARNING() {
        return 0x04;
    }
    static get NOTICE() {
        return 0x05;
    }
    static get INFO() {
        return 0x06;
    }
    static get DEBUG() {
        return 0x07;
    }
}

module.exports = LogLevel;
